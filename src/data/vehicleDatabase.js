export function getBrands() { return [...new Set(getVehicles().map(v => v.brand))].sort() }
export function getVehicleById(id) { return getVehicles().find(v => v.id === id) || null }

export function searchVehicles({ brand = '', fuelType = '', bodyType = '', query = '', maxPrice = 0, minPrice = 0 } = {}) {
  return getVehicles().filter(v => {
    if (brand && v.brand !== brand) return false
    if (fuelType && v.fuel !== fuelType) return false
    if (bodyType && v.bodyType !== bodyType) return false
    if (maxPrice > 0 && v.price > maxPrice) return false
    if (minPrice > 0 && v.price < minPrice) return false
    if (query) {
      const q = query.toLowerCase()
      return (v.brand + ' ' + v.model + ' ' + v.variant).toLowerCase().includes(q)
    }
    return true
  })
}
